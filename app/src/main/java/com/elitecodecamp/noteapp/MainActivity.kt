@Composable
fun NotesApp(noteViewModel: NoteViewModel = viewModel()){

    val notesList = noteViewModel.getAllNotes()

    NoteScreen(notes = notesList,
        onAddNote = {note ->
            noteViewModel.addNote(note)
        },
        onRemoveNote = {note ->

            noteViewModel.removeNote(note)

        })
}



